#include "so_long.h"

void	player_pos(t_game *game)
{
	int (i), (j);
	i = 0;
	while (game->map[i])
	{
		j = 0;
		while (game->map[i][j])
		{
			if (game->map[i][j] == 'P')
			{
				game->p_x = j;
				game->p_y = i;
				return ;
			}
			j++;
		}
		i++;
	}
}
